package EJ4;

public class Megaproducto {
    public int calificacionennumero;

    public Megaproducto(int calificacionennumero) {
        this.calificacionennumero = calificacionennumero;
    }

    @Override
    public String toString() {
        return "Megaproducto{" +
                "calificacionennumero=" + calificacionennumero +
                '}';
    }
}
